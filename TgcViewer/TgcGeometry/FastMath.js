var FastMath = {};
 

 
/**
 * Representa la relación entre la longitud de la circunferencia de un círculo y su diámetro, especificada por la constante PI.
 * @type float
 */
FastMath.PI = Math.PI;


/**
 * PI / 2
 * @type float
 */
FastMath.PI_HALF = FastMath.PI / 2.0;

/**
 * 2 PI
 * @type float
 */
FastMath.TWO_PI = FastMath.PI * 2.0;


/**
 * Vector3(0, 0, 0)
 * @type vec3
 */
FastMath.CERO_VEC3 = vec3.createFrom(0, 0, 0);





